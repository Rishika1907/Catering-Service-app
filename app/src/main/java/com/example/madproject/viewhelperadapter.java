package com.example.madproject;

public class viewhelperadapter {

    String item,value;

    //Constructor
    public viewhelperadapter(String item, String value) {
        this.item = item;
        this.value = value;
    }

    //Getter Methods
    public String getItem() {
        return item;
    }

    public String getValue() {
        return value;
    }
}
